#include <cstdio>                      // puts, printf      
#include <iostream>                    // cout, endl  
using namespace std;                   // 使用名字空间std（的任何名字）    
 
int main()
{
	puts("1. Hello, world!");          // 输出   
	printf("2. Hello, world!\n");      // 输出  
	cout << "3. Hello, world!" << endl;      // 输出     
}